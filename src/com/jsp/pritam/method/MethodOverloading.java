public class MethodOverloading {

	/*
	 * Method Overloading ->
	 * Having Multiple methods in a class with the same name but changing in the signature is called method overloading.
	 * -> In method Overloading return type does not matter.
	 */
	
	void  admision(String name, int age) { // Number of parameter - 2
		// Logic
	}
	
	//Overloading
	void admision(int mobileno) { // Number of parameter - 1
		//Logic
	}
}

// Method overloading is the way to achieve compile time Polymorphism.
